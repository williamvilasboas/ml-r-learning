print("Trabalhando com matriz")

matrizA <- matrix(c(2, 4, 3, 1, 5, 7), nrow = 2,ncol = 3,byrow = TRUE)

matrizB <- matrix(c(3, 4, 5, 6, 7, 8), nrow = 2, ncol = 3, byrow = TRUE)
print(matrizA)
print(matrizB)

print(matrizA * matrizB) # Multiplicação de matrizes

print(matrizA + matrizB) # Soma de Matrizes

print(matrizA / matrizB) # Divisão